#!/usr/bin/env python3
"""
Type-annotated function concat.
"""


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings and returns the combined string.

    Args:
        str1: The first string to concatenate.
        str2: The second string to concatenate.

    Returns:
        The combined string of str1 and str2.
    """

    return str1 + str2
